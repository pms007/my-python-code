a = 8

b = 4

print( 'Assign Values:\t\t','a =' , a , '\t b =' , b )

a += b

print( 'Add & Assign:\t\t','(a += b) Now a is' , a)

a -= b

print( 'Sub & Assign:\t\t','(a -= b) Now a is' , a)

a *= b

print( 'Multiply & Assign:\t\t','(a *= b) Now a is' , a)


a /= b

print( 'Division & Assign:\t\t','(a /= b) Now a is' , a)


a %= b

print( 'Modulus & Assign:\t\t','(a %= b) Now a is' , a)
